// ===============================
//  Simple Function (Named Function)
// ===============================

function greet() {
    // This function prints a message
    console.log("Hello World");
}

// Calling the function
greet();


// ===============================
//  Named Function Example
// ===============================

function myfunction() {
    // Declaring variables
    var x = 10;
    var y = 90;

    // Printing sum
    console.log(x + y);
}

// Calling the function
myfunction();